#include <iostream>

using namespace std;

int main() {
	
	int a = 7;
	int b = 0;

	cout << "a == " << a << endl;
	cout << "b == " << b << endl;	
	cout << "The sum a + b == " << a + b << endl;
	cout << "The difference a - b == " << a - b << endl;
	cout << "The product a * b == " << a * b << endl;
	cout << "The quotient a/b == " << a/b << endl; // Note that the quotient gives only the integer part of the division!
	cout << "The remainder a % b == " << a % b << endl;

	return 0;
}
