#include <iostream>

using namespace std;

int main()
{
	
	int a = 1;
	int b = 2;

	cout << "The value of a is " << a << " and the value of b is " << b << endl;
	
	// Assign new values to a and b using the assignment operator = 
	a = 3;
	b = 5;

	cout <<	"The value of a is now " << a << " and the value of b is now " << b << endl;
	
	return 0;
}
