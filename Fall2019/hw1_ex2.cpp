#include <iostream>

using namespace std;

int main(){

  double a, b;
  cout << "Please enter value a: ";
  cin >> a;
  cout << "Please enter value b: ";
  cin >> b;

  cout << "Sum: " << a+b << endl;
  cout << "Difference: " << a-b << endl;
  cout << "Product: " << a*b << endl;

  cout << "a+b = " << a+b << endl;
  cout << "a-b = " << a-b << endl;
  cout << "a*b = " << a*b << endl;

  return 0;

}
